export const colorCode = (band) => {
  return COLORS.indexOf(band);  // retorna o valor do índice da posição que está band no array COLORS
};

export const COLORS = ['black', 'brown', 'red', 'orange', 'yellow', 'green', 'blue', 'violet', 'grey', 'white'];  // declaração do array e inserção de itens
